package com.supertool.dspui.param.form;

public class SettingForm {
	private int flv_size;
	private int swf_size;
	private int img_size;
	
	public int getFlv_size() {
		return flv_size;
	}
	public void setFlv_size(int flv_size) {
		this.flv_size = flv_size;
	}
	public int getSwf_size() {
		return swf_size;
	}
	public void setSwf_size(int swf_size) {
		this.swf_size = swf_size;
	}
	public int getImg_size() {
		return img_size;
	}
	public void setImg_size(int img_size) {
		this.img_size = img_size;
	}
	
}
