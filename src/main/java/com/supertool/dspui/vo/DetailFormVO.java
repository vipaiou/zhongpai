package com.supertool.dspui.vo;

public class DetailFormVO {
	
	private int adplacementId;
	private int bid;
	private int winBid;
	private int pv;
	private int click;
	private double cost;
	
	public int getAdplacementId() {
		return adplacementId;
	}
	public void setAdplacementId(int adplacementId) {
		this.adplacementId = adplacementId;
	}
	public int getBid() {
		return bid;
	}
	public void setBid(int bid) {
		this.bid = bid;
	}
	public int getWinBid() {
		return winBid;
	}
	public void setWinBid(int winBid) {
		this.winBid = winBid;
	}
	public int getPv() {
		return pv;
	}
	public void setPv(int pv) {
		this.pv = pv;
	}
	public int getClick() {
		return click;
	}
	public void setClick(int click) {
		this.click = click;
	}
	public double getCost() {
		return cost;
	}
	public void setCost(double cost) {
		this.cost = cost;
	}
	
}
