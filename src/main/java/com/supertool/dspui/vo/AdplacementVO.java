package com.supertool.dspui.vo;

public class AdplacementVO {
	
	private String id;
	private String name;
	private String allowedTypes;
	private String bidFloor;
	private String height;
	private String width;
	private String blockCategory;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getAllowedTypes() {
		return allowedTypes;
	}
	public void setAllowedTypes(String allowedTypes) {
		this.allowedTypes = allowedTypes;
	}
	public String getBidFloor() {
		return bidFloor;
	}
	public void setBidFloor(String bidFloor) {
		this.bidFloor = bidFloor;
	}
	public String getHeight() {
		return height;
	}
	public void setHeight(String height) {
		this.height = height;
	}
	public String getWidth() {
		return width;
	}
	public void setWidth(String width) {
		this.width = width;
	}
	public String getBlockCategory() {
		return blockCategory;
	}
	public void setBlockCategory(String blockCategory) {
		this.blockCategory = blockCategory;
	}
	

}
