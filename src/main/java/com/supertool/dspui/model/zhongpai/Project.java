package com.supertool.dspui.model.zhongpai;

public class Project {
	private String category_id;
	private String name;
	private String province;
	private String city;
	private String summary;
	private String video_url;
	private String risk;
	private String target_amount;
	private String target_days;
	public String getCategory_id() {
		return category_id;
	}
	public void setCategory_id(String category_id) {
		this.category_id = category_id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getProvince() {
		return province;
	}
	public void setProvince(String province) {
		this.province = province;
	}
	public String getCity() {
		return city;
	}
	public void setCity(String city) {
		this.city = city;
	}
	public String getSummary() {
		return summary;
	}
	public void setSummary(String summary) {
		this.summary = summary;
	}
	public String getVideo_url() {
		return video_url;
	}
	public void setVideo_url(String video_url) {
		this.video_url = video_url;
	}
	public String getRisk() {
		return risk;
	}
	public void setRisk(String risk) {
		this.risk = risk;
	}
	public String getTarget_amount() {
		return target_amount;
	}
	public void setTarget_amount(String target_amount) {
		this.target_amount = target_amount;
	}
	public String getTarget_days() {
		return target_days;
	}
	public void setTarget_days(String target_days) {
		this.target_days = target_days;
	}
}
